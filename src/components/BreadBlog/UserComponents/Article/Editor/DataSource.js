const MarkDownExtension = {
	name: 'MarkExtension',
	level: 'inline',
	start: (text) => text.match(/@[^@]/)?.index,
	tokenizer(text) {
		const reg = /^@([^@]*)@/;
		const match = reg.exec(text);

		if (match) {
			const token = {
				type: 'MarkExtension',
				raw: match[0],
				text: match[1].trim(),
				tokens: []
			};

			return token;
		}
	},
	renderer(token) {
		return `<mark>${token.text}</mark>`;
	}
};

